//5.4 Distintas formas de crear objetos

//caso objeto 1
let miObjeto = new Object(); //Esta es una opción formal

//caso objeto 2
let miObjeto2 = {}; //Opción breve y recomendada

//caso String 1
let miCadena = new String("Hola"); //Sintaxis formal

//caso String 2
let miCadena2 = "Hola"; //Sintaxis breve y recomendada

//caso con números 1
let miNumero = new Number(1); //Sintaxis formal

//caso con números 2
let miNumero2 = 1; //Sintaxis breve y recomendada

//caso con boolean 1
let miBoolean = new Boolean(false); //Sintaxis formal

//caso con boolean 2
let miBoolean2 = false; //Sintaxis breve y recomendada

//caso con arreglos 1
let miArreglo = new Array(); //Sintaxis formal

//caso con arreglos 2
let miArreglo2 = []; //Sintaxis breve y recomendada

//caso con funciones 1
let miFuncion = new Function(); //Sintaxis formal

//caso con funciones 2
let miFuncion2 = function(){}; //Sintaxis breve y recomendada
